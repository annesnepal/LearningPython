#List

fruits = ['Litchee', 'Mango', 'Banana', 'Apple']
print(fruits)

#Length of List
print(len(fruits))

#Data Type
print(type(fruits))

#Indexing
print(fruits[2])

#Adding item to the list

fruits.append('Grapes')
print(fruits)

